public class Scroll {
    private final String name;
    private final Enchantment1 enchant;
    private final int level;

    public Scroll(String name, Enchantment1 enchant, int level) {
        this.name = name;
        this.enchant = enchant;
        this.level = level;
    }

    public String getName(){ return name; }
    public Enchantment1 getEnchantment(){ return enchant; }
    public String getLevel(){ return name; }

}
